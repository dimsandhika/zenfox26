function processSentence(name, age, address, hobby) {

    return 'Nama saya ' + name + ',' + ' umur saya ' + age + ' tahun' + ',' + ' alamat saya di ' + address + ',' + ' dan saya punya hobby yaitu ' + hobby + '!'
}
console.log(processSentence('Agus', 30, 'Jln. Malioboro, Yogjakarta', 'gaming')); // Menampilkan "Nama saya Agus, umur saya 30 tahun, alamat saya di Jln. Malioboro, Yogjakarta, dan saya punya hobby yaitu gaming!"